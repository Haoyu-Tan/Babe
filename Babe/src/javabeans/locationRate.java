package javabeans;

public class locationRate {
	
	private String profileID;
	private int rate;
	
	public locationRate() {
		
	}
	
	public String getProfileID() {
		return profileID;
	}
	public void setProfileID(String profileID) {
		this.profileID = profileID;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}

}
